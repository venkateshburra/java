package dataTypes;
 class dataTypes {
    public static void main(String args[]) {
        int i = 10;
        short s = 20;
        long l = 5543l;
        byte b = 122;
        boolean be = true;
        char c = 'a';
        c++;
        float f = 30.5f;
        double d = 20.456;
        
        System.out.println(i);
        System.out.println(s);
        System.out.println(l);
        System.out.println(b);
        System.out.println(be);
        System.out.println(c);
        System.out.println(f);
        System.out.println(d);
    }
}
